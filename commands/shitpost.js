Parser.commands.shitpost = function(user, room, arg) {
  if (room === null || !isPrivate(room)) return false;

  let message = arg.removeAccents().trim();
  if (message.length > 15) {
    return {msg: 'Testo troppo lungo'};
  }

  let text0 = '';
  let text1 = '';
  let text2 = '';

  if (message === '') {
    message = 'SHITPOST';
  }

  message = message.split('');
  for (const i in message) {
    if (letters[message[i]]) {
      if (text0 !== '') {
        text0 += ' ';
        text1 += ' ';
        text2 += ' ';
      }
      text0 += letters[message[i]][0];
      text1 += letters[message[i]][1];
      text2 += letters[message[i]][2];
    }
  }

  Chat.sendHTMLBox(room, '<pre style="margin:0">' + text0 + '\n' + text1 + '\n' + text2 + '</pre>');

  return false;
};

